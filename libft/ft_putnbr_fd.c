#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	if (n < 0 && n > -2147483648)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n > 9 && n <= 2147483647)
	{
		ft_putnbr_fd(n / 10, fd);
		c = (n % 10) + '0';
		write(fd, &c, 1);
	}
	if (n <= 9 && n >= 0)
	{
		c = n + '0';
		write(fd, &c, 1);
	}
	return ;
}
